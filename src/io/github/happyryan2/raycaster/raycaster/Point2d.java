/*
This class is functionally identical to java.awt.Point, but its coordinates accept non-integer values.
*/

package io.github.happyryan2.raycaster.raycaster;

public class Point2d {
	public float x;
	public float y;
	public Point2d(float x, float y) {
		this.x = x;
		this.y = y;
	}
}
